android {
    namespace = "com.snapfacture"
    compileSdk = 35

    defaultConfig {
        applicationId = "com.snapfacture"
        minSdk = 26
        targetSdk = 35
        // Overridable from CI so tagged releases carry a real version:
        // ./gradlew assembleRelease -PversionCode=42 -PversionName=1.2.0
        versionCode = (project.findProperty("versionCode") as String?)?.toIntOrNull() ?: 1
        versionName = (project.findProperty("versionName") as String?) ?: "1.0.0"

        testInstrumentationRunner = "androidx.test.runner.AndroidJUnitRunner"
        vectorDrawables { useSupportLibrary = true }
    }

    // A stable release key is what lets users update without uninstalling
    // (and losing their local database). Provided via CI secrets; absent
    // locally, the release build simply stays unsigned.
    val keystorePath = System.getenv("RELEASE_KEYSTORE_PATH")
    if (keystorePath != null) {
        signingConfigs {
            create("release") {
                storeFile = file(keystorePath)
                storePassword = System.getenv("RELEASE_KEYSTORE_PASSWORD")
                keyAlias = System.getenv("RELEASE_KEY_ALIAS")
                keyPassword = System.getenv("RELEASE_KEY_PASSWORD")
            }
        }
    }

    buildTypes {
        release {
            isMinifyEnabled = true
            isShrinkResources = true
            proguardFiles(
                getDefaultProguardFile("proguard-android-optimize.txt"),
                "proguard-rules.pro"
            )
            signingConfig = signingConfigs.findByName("release")
        }
        debug {
            applicationIdSuffix = ".debug"
        }
    }

    compileOptions {
        sourceCompatibility = JavaVersion.VERSION_17
        targetCompatibility = JavaVersion.VERSION_17
    }

    kotlinOptions {
        jvmTarget = "17"
    }

    buildFeatures {
        compose = true
        buildConfig = true
    }

    packaging {
        resources.excludes += "/META-INF/{AL2.0,LGPL2.1}"
    }

    testOptions {
        unitTests {
            isIncludeAndroidResources = true
            all { test ->
                // Print the actual assertion message in CI logs; gradle's
                // default one-line summary hides it.
                test.testLogging {
                    events("failed")
                    setExceptionFormat("full")
                }
            }
        }
    }
}

